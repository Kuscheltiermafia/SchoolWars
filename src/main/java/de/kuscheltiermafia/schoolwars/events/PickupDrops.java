package de.kuscheltiermafia.schoolwars.events;

import de.kuscheltiermafia.schoolwars.items.Items;
import org.bukkit.entity.Item;
import org.bukkit.entity.Player;
import org.bukkit.event.EventHandler;
import org.bukkit.event.Listener;
import org.bukkit.event.entity.ItemDespawnEvent;
import org.bukkit.event.player.PlayerInteractEntityEvent;
import org.bukkit.inventory.ItemStack;

public class PickupDrops implements Listener {

    @EventHandler
    public void onDropPickup(PlayerInteractEntityEvent e) {

        try {
            if (Items.itemHitboxes.containsKey(e.getRightClicked())) {
                Player p = e.getPlayer();
                Item item = Items.itemHitboxes.get(e.getRightClicked());
                p.getInventory().addItem(new ItemStack(item.getItemStack()));
                item.remove();
                e.getRightClicked().remove();
                Items.itemHitboxes.remove(e.getRightClicked());
            }
        }catch (Exception ignored){}
    }

    @EventHandler
    public void onItemDespawn(ItemDespawnEvent e){
        e.setCancelled(true);
    }

}
